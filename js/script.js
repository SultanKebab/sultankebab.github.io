translations.en = translations.de;
translations.tr = translations.de;
const currentLang = localStorage.getItem("sultan-lang") || "de";
function applyTranslations(lang) {
  const dict = translations[lang] || translations.de;
  document.documentElement.lang = lang === "tr" ? "tr" : (lang === "en" ? "en" : "de");
  document.querySelectorAll("[data-i18n]").forEach(el => {
    const key = el.dataset.i18n;
    if (dict[key] !== undefined) el.textContent = dict[key];
  });
  document.querySelectorAll("[data-i18n-html]").forEach(el => {
    const key = el.dataset.i18nHtml;
    if (dict[key] !== undefined) el.innerHTML = dict[key];
  });
  document.querySelectorAll("[data-meta-title]").forEach(el => {
    const key = el.dataset.metaTitle;
    if (dict[key] !== undefined) {
      if (el.tagName.toLowerCase() === "title") el.textContent = dict[key];
      else el.setAttribute("content", dict[key]);
    }
  });
  document.querySelectorAll("[data-meta-description]").forEach(el => {
    const key = el.dataset.metaDescription;
    if (dict[key] !== undefined) el.setAttribute("content", dict[key]);
  });
  document.querySelectorAll(".lang-btn").forEach(btn => {
    btn.classList.toggle("active", btn.dataset.lang === lang);
  });
  localStorage.setItem("sultan-lang", lang);
}
document.querySelectorAll(".lang-btn").forEach(btn => {
  btn.addEventListener("click", () => applyTranslations(btn.dataset.lang));
});
applyTranslations(currentLang);
